public class Objective7Lab1 {
  public static void main(String[] args) {
    int counter = 10;   // counter variable starting value

    while(counter > 0) {
      // Print the value of counter with each loop iteration
      System.out.println(counter);
      counter = counter - 1;   // this line counts down, or decrements the counter variable 
    }
  }
}
